# Example variable
age = 20

# Simple if statement
if age >= 18:
    print("You are an adult.")

# if-else statement
if age < 18:
    print("You are a minor.")
else:
    print("You are an adult.")

# if-elif-else ladder
if age < 13:
    print("You are a child.")
elif age < 20:
    print("You are a teenager.")
else:
    print("You are an adult.")
