myTup = ()
print("Empty tuple is ",myTup)

# Using String
myTup1 = ('Geeks', 'For')
print("Tuple using String is ", myTup1)

# Using List
myList = [1, 2, 4, 5, 6]
print("Conversion of list to tuple is ",tuple(myList))

# Using Built-in Function
myTup3 = tuple('Geeks')
print("Tuple using built in function is ",myTup3)

#Create tuple with repetition
myTup4 = ('Example',)*5
print("Tuple using *  is ",myTup4)
