# Integer
x = 10
print("Value:", x, "| Type:", type(x))   # <class 'int'>

# Float
y = 3.14
print("Value:", y, "| Type:", type(y))   # <class 'float'>

# String
name = "Alice"
print("Value:", name, "| Type:", type(name))  # <class 'str'>

# Boolean
flag = True
print("Value:", flag, "| Type:", type(flag))  # <class 'bool'>

# Complex number
z = 2 + 3j
print("Value:", z, "| Type:", type(z))  # <class 'complex'>

# Bytes
b = b"Hello"
print("Value:", b, "| Type:", type(b))  # <class 'bytes'>
